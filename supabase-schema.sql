-- 1. SAFE DROP FOR NEW AND EXISTING DATABASES
-- Drop trigger safely only if matches table exists
DO $$
BEGIN
    IF EXISTS (SELECT 1 FROM pg_tables WHERE tablename = 'matches') THEN
        DROP TRIGGER IF EXISTS trg_match_completed ON matches;
    END IF;
END $$;

DROP FUNCTION IF EXISTS update_points_on_match_completion();
DROP TABLE IF EXISTS registrations;
DROP TABLE IF EXISTS news;
DROP TABLE IF EXISTS points_table;
DROP TABLE IF EXISTS matches;
DROP TABLE IF EXISTS players;
DROP TABLE IF EXISTS teams;

-- 2. CREATE TABLES
CREATE TABLE teams (
    id INTEGER PRIMARY KEY,
    name TEXT NOT NULL UNIQUE,
    city TEXT NOT NULL,
    logo TEXT NOT NULL,
    color TEXT NOT NULL,
    captain TEXT NOT NULL
);

CREATE TABLE players (
    id INTEGER PRIMARY KEY,
    team_id INTEGER REFERENCES teams(id) ON DELETE SET NULL,
    name TEXT NOT NULL,
    role TEXT NOT NULL,
    jersey INTEGER NOT NULL,
    photo TEXT,
    runs INTEGER DEFAULT 0,
    wickets INTEGER DEFAULT 0,
    strike_rate NUMERIC DEFAULT 0.0,
    fifties INTEGER DEFAULT 0,
    economy NUMERIC DEFAULT 0.0,
    fours INTEGER DEFAULT 0,
    sixes INTEGER DEFAULT 0,
    boundaries INTEGER DEFAULT 0
);

CREATE TABLE matches (
    id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    stage TEXT DEFAULT 'LEAGUE MATCH',
    date TEXT NOT NULL,
    venue TEXT NOT NULL,
    team1_id INTEGER REFERENCES teams(id) ON DELETE CASCADE,
    team2_id INTEGER REFERENCES teams(id) ON DELETE CASCADE,
    score_team1 TEXT DEFAULT '0/0 (0 Ov)',
    score_team2 TEXT DEFAULT '0/0 (0 Ov)',
    summary TEXT DEFAULT 'Match scheduled',
    status TEXT DEFAULT 'upcoming' CHECK (status IN ('upcoming', 'live', 'completed')),
    winner_team_id INTEGER REFERENCES teams(id) ON DELETE SET NULL
);

CREATE TABLE points_table (
    id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    team_id INTEGER REFERENCES teams(id) ON DELETE CASCADE UNIQUE,
    played INTEGER DEFAULT 0,
    won INTEGER DEFAULT 0,
    lost INTEGER DEFAULT 0,
    nr INTEGER DEFAULT 0,
    points INTEGER DEFAULT 0,
    nrr NUMERIC DEFAULT 0.0,
    position INTEGER DEFAULT 1,
    last_five TEXT[] DEFAULT '{}'::TEXT[]
);

CREATE TABLE news (
    id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    title TEXT NOT NULL,
    excerpt TEXT NOT NULL,
    content TEXT,
    image TEXT NOT NULL,
    tag TEXT NOT NULL,
    date TIMESTAMPTZ DEFAULT TIMEZONE('utc'::text, NOW())
);

CREATE TABLE registrations (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    full_name TEXT NOT NULL,
    whatsapp TEXT NOT NULL,
    email TEXT NOT NULL,
    district TEXT NOT NULL,
    pincode TEXT NOT NULL,
    role TEXT NOT NULL,
    payment_status TEXT DEFAULT 'pending',
    created_at TIMESTAMPTZ DEFAULT TIMEZONE('utc'::text, NOW())
);

-- 3. ENABLE REALTIME ON MATCHES
-- Supabase requires explicit publication enrollment for realtime events
ALTER PUBLICATION supabase_realtime ADD TABLE matches;

-- 4. CREATE POINTS TABLE TRIGGERS FOR MATCH COMPLETION
CREATE OR REPLACE FUNCTION update_points_on_match_completion()
RETURNS TRIGGER AS $$
DECLARE
    team1_pts_exist BOOLEAN;
    team2_pts_exist BOOLEAN;
BEGIN
    -- Only run when status changes to 'completed'
    IF NEW.status = 'completed' AND (OLD.status IS NULL OR OLD.status != 'completed') THEN
        
        -- Ensure team1 exists in points table
        SELECT EXISTS(SELECT 1 FROM points_table WHERE team_id = NEW.team1_id) INTO team1_pts_exist;
        IF NOT team1_pts_exist THEN
            INSERT INTO points_table (team_id, played, won, lost, points, nrr, last_five)
            VALUES (NEW.team1_id, 0, 0, 0, 0, 0.0, '{}');
        END IF;

        -- Ensure team2 exists in points table
        SELECT EXISTS(SELECT 1 FROM points_table WHERE team_id = NEW.team2_id) INTO team2_pts_exist;
        IF NOT team2_pts_exist THEN
            INSERT INTO points_table (team_id, played, won, lost, points, nrr, last_five)
            VALUES (NEW.team2_id, 0, 0, 0, 0, 0.0, '{}');
        END IF;

        -- 1. Increment played count for both teams
        UPDATE points_table SET played = played + 1 WHERE team_id = NEW.team1_id OR team_id = NEW.team2_id;

        -- 2. Distribute points based on winner
        IF NEW.winner_team_id IS NOT NULL THEN
            -- Update winner stats (+1 Win, +2 Points, Form 'W')
            UPDATE points_table 
            SET won = won + 1,
                points = points + 2,
                last_five = array_append(last_five, 'W')
            WHERE team_id = NEW.winner_team_id;

            -- Update loser stats (+1 Loss, Form 'L')
            IF NEW.winner_team_id = NEW.team1_id THEN
                UPDATE points_table 
                SET lost = lost + 1,
                    last_five = array_append(last_five, 'L')
                WHERE team_id = NEW.team2_id;
            ELSE
                UPDATE points_table 
                SET lost = lost + 1,
                    last_five = array_append(last_five, 'L')
                WHERE team_id = NEW.team1_id;
            END IF;
        ELSE
            -- Draw / No Result (each team get +1 points, Form 'D' or '-' if needed, we'll append 'D')
            UPDATE points_table 
            SET nr = nr + 1,
                points = points + 1,
                last_five = array_append(last_five, 'NR')
            WHERE team_id = NEW.team1_id OR team_id = NEW.team2_id;
        END IF;
    END IF;
    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

CREATE TRIGGER trg_match_completed
AFTER UPDATE ON matches
FOR EACH ROW
EXECUTE FUNCTION update_points_on_match_completion();

